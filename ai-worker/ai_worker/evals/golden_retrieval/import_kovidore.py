"""Dev DB importer for KoViDoRe SearchUnit fixtures.

This intentionally bypasses the OCR callback path. KoViDoRe markdown is already
treated as an OCR_RESULT_JSON fixture, so the importer only seeds catalog rows
that the existing SearchUnit indexing claim can pick up.
"""

from __future__ import annotations

import argparse
import json
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Iterable, Optional

from app.core.config import WorkerSettings


@dataclass(frozen=True)
class FixtureBundle:
    source_files: list[dict[str, Any]]
    extracted_artifacts: list[dict[str, Any]]
    search_units: list[dict[str, Any]]
    manifest: dict[str, Any]


@dataclass(frozen=True)
class ImportSummary:
    source_files: int
    extracted_artifacts: int
    search_units: int


def load_fixture_bundle(fixture_dir: Path) -> FixtureBundle:
    return FixtureBundle(
        source_files=_read_jsonl(fixture_dir / "source_files.jsonl"),
        extracted_artifacts=_read_jsonl(fixture_dir / "extracted_artifacts.jsonl"),
        search_units=_read_jsonl(fixture_dir / "search_units.jsonl"),
        manifest=_read_json(fixture_dir / "manifest.json"),
    )


def import_fixture(
    *,
    fixture_dir: Path,
    db_dsn: str,
    batch_size: int,
    reset_embedding_status: bool = False,
) -> ImportSummary:
    try:
        import psycopg2  # type: ignore[import-not-found]
    except ImportError as exc:
        raise RuntimeError("KoViDoRe DB import requires psycopg2-binary") from exc

    bundle = load_fixture_bundle(fixture_dir)
    conn = psycopg2.connect(db_dsn)
    try:
        with conn:
            with conn.cursor() as cursor:
                upsert_fixture_rows(
                    cursor,
                    bundle,
                    batch_size=batch_size,
                    reset_embedding_status=reset_embedding_status,
                )
    finally:
        conn.close()
    return ImportSummary(
        source_files=len(bundle.source_files),
        extracted_artifacts=len(bundle.extracted_artifacts),
        search_units=len(bundle.search_units),
    )


def upsert_fixture_rows(
    cursor: Any,
    bundle: FixtureBundle,
    *,
    batch_size: int = 100,
    reset_embedding_status: bool = False,
    now: Optional[datetime] = None,
) -> None:
    now = now or datetime.now(timezone.utc)
    safe_batch_size = max(1, int(batch_size or 100))

    for artifact_batch in _batched(bundle.extracted_artifacts, safe_batch_size):
        for artifact in artifact_batch:
            _upsert_job(cursor, artifact, now)

    for source_batch in _batched(bundle.source_files, safe_batch_size):
        for source in source_batch:
            _upsert_source_file(cursor, source, now)

    for artifact_batch in _batched(bundle.extracted_artifacts, safe_batch_size):
        for artifact in artifact_batch:
            _upsert_artifact(cursor, artifact, now)
            _upsert_extracted_artifact(cursor, artifact, now)

    artifact_by_source = {
        str(artifact["sourceFileId"]): str(artifact["artifactId"])
        for artifact in bundle.extracted_artifacts
    }
    for unit_batch in _batched(bundle.search_units, safe_batch_size):
        for unit in unit_batch:
            artifact_id = str(unit.get("extractedArtifactId") or artifact_by_source.get(str(unit["sourceFileId"])))
            _upsert_search_unit(
                cursor,
                unit,
                artifact_id=artifact_id,
                reset_embedding_status=reset_embedding_status,
                now=now,
            )


def main(argv: Optional[list[str]] = None) -> int:
    parser = _build_parser()
    args = parser.parse_args(argv)
    settings = WorkerSettings()
    db_dsn = args.db_dsn or settings.rag_db_dsn
    summary = import_fixture(
        fixture_dir=Path(args.fixture_dir),
        db_dsn=db_dsn,
        batch_size=args.batch_size,
        reset_embedding_status=args.reset_embedding_status,
    )
    print(json.dumps({
        "sourceFiles": summary.source_files,
        "extractedArtifacts": summary.extracted_artifacts,
        "searchUnits": summary.search_units,
        "coreApiUrl": args.core_api_url,
        "dbImport": True,
    }, ensure_ascii=False, indent=2))
    return 0


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Import KoViDoRe dev/eval fixtures into the Spring catalog DB.",
    )
    parser.add_argument("--fixture-dir", required=True, help="Directory generated by adapters.kovidore.")
    parser.add_argument(
        "--core-api-url",
        default="http://localhost:8080",
        help="Accepted for workflow symmetry; the current safe importer seeds the dev DB directly.",
    )
    parser.add_argument(
        "--db-dsn",
        default=None,
        help="libpq DSN for the Spring dev DB. Defaults to WorkerSettings.rag_db_dsn.",
    )
    parser.add_argument("--batch-size", type=int, default=100, help="Rows per logical import batch.")
    parser.add_argument(
        "--reset-embedding-status",
        action="store_true",
        help="Reset matching SearchUnits to PENDING even when content is unchanged.",
    )
    return parser


def _upsert_job(cursor: Any, artifact: dict[str, Any], now: datetime) -> None:
    cursor.execute(
        """
        INSERT INTO job
            (id, capability, status, attempt_no, created_at, updated_at)
        VALUES
            (%s, %s, %s, %s, %s, %s)
        ON CONFLICT (id) DO UPDATE SET
            capability = EXCLUDED.capability,
            status = EXCLUDED.status,
            updated_at = EXCLUDED.updated_at
        """,
        (
            artifact["jobId"],
            "DEV_FIXTURE_IMPORT",
            "COMPLETED",
            1,
            now,
            now,
        ),
    )


def _upsert_source_file(cursor: Any, source: dict[str, Any], now: datetime) -> None:
    cursor.execute(
        """
        INSERT INTO source_file
            (id, original_file_name, mime_type, file_type, storage_uri,
             status, status_detail, uploaded_at, updated_at)
        VALUES
            (%s, %s, %s, %s, %s, %s, %s, %s, %s)
        ON CONFLICT (id) DO UPDATE SET
            original_file_name = EXCLUDED.original_file_name,
            mime_type = EXCLUDED.mime_type,
            file_type = EXCLUDED.file_type,
            storage_uri = EXCLUDED.storage_uri,
            status = EXCLUDED.status,
            status_detail = EXCLUDED.status_detail,
            updated_at = EXCLUDED.updated_at
        """,
        (
            source["sourceFileId"],
            source["sourceFileName"],
            source.get("mimeType") or "application/pdf",
            source.get("fileType") or "pdf",
            source.get("storageUri") or f"fixture://kovidore-v2-economic-beir/{source['sourceFileName']}.pdf",
            source.get("status") or "READY",
            "KoViDoRe dev/eval fixture import",
            now,
            now,
        ),
    )


def _upsert_artifact(cursor: Any, artifact: dict[str, Any], now: datetime) -> None:
    payload_text = _json_text(artifact.get("payloadJson"))
    cursor.execute(
        """
        INSERT INTO artifact
            (id, job_id, role, type, storage_uri, content_type, size_bytes,
             checksum_sha256, created_at)
        VALUES
            (%s, %s, %s, %s, %s, %s, %s, %s, %s)
        ON CONFLICT (id) DO UPDATE SET
            job_id = EXCLUDED.job_id,
            role = EXCLUDED.role,
            type = EXCLUDED.type,
            storage_uri = EXCLUDED.storage_uri,
            content_type = EXCLUDED.content_type,
            size_bytes = EXCLUDED.size_bytes,
            checksum_sha256 = EXCLUDED.checksum_sha256
        """,
        (
            artifact["artifactId"],
            artifact["jobId"],
            "OUTPUT",
            artifact.get("artifactType") or "OCR_RESULT_JSON",
            artifact["storageUri"],
            "application/json",
            len(payload_text.encode("utf-8")),
            artifact.get("checksumSha256"),
            now,
        ),
    )


def _upsert_extracted_artifact(cursor: Any, artifact: dict[str, Any], now: datetime) -> None:
    cursor.execute(
        """
        INSERT INTO extracted_artifact
            (artifact_id, source_file_id, artifact_type, artifact_key, storage_uri,
             pipeline_version, checksum_sha256, payload_json, created_at, updated_at)
        VALUES
            (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
        ON CONFLICT (artifact_id) DO UPDATE SET
            source_file_id = EXCLUDED.source_file_id,
            artifact_type = EXCLUDED.artifact_type,
            artifact_key = EXCLUDED.artifact_key,
            storage_uri = EXCLUDED.storage_uri,
            pipeline_version = EXCLUDED.pipeline_version,
            checksum_sha256 = EXCLUDED.checksum_sha256,
            payload_json = EXCLUDED.payload_json,
            updated_at = EXCLUDED.updated_at
        """,
        (
            artifact["artifactId"],
            artifact["sourceFileId"],
            artifact.get("artifactType") or "OCR_RESULT_JSON",
            artifact["artifactKey"],
            artifact["storageUri"],
            artifact.get("pipelineVersion") or "kovidore-fixture-v1",
            artifact.get("checksumSha256"),
            _json_text(artifact.get("payloadJson")),
            now,
            now,
        ),
    )


def _upsert_search_unit(
    cursor: Any,
    unit: dict[str, Any],
    *,
    artifact_id: str,
    reset_embedding_status: bool,
    now: datetime,
) -> None:
    cursor.execute(
        """
        INSERT INTO search_unit
            (id, source_file_id, extracted_artifact_id, unit_type, unit_key, title,
             section_path, page_start, page_end, text_content, metadata_json,
             embedding_status, content_sha256, index_id, created_at, updated_at)
        VALUES
            (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
        ON CONFLICT (source_file_id, unit_type, unit_key) DO UPDATE SET
            extracted_artifact_id = EXCLUDED.extracted_artifact_id,
            title = EXCLUDED.title,
            section_path = EXCLUDED.section_path,
            page_start = EXCLUDED.page_start,
            page_end = EXCLUDED.page_end,
            text_content = EXCLUDED.text_content,
            metadata_json = EXCLUDED.metadata_json,
            content_sha256 = EXCLUDED.content_sha256,
            index_id = EXCLUDED.index_id,
            embedding_status = CASE
                WHEN %s THEN 'PENDING'
                WHEN search_unit.content_sha256 IS DISTINCT FROM EXCLUDED.content_sha256 THEN 'PENDING'
                ELSE search_unit.embedding_status
            END,
            indexed_content_sha256 = CASE
                WHEN %s THEN NULL
                WHEN search_unit.content_sha256 IS DISTINCT FROM EXCLUDED.content_sha256 THEN NULL
                ELSE search_unit.indexed_content_sha256
            END,
            embedding_claim_token = CASE
                WHEN %s THEN NULL
                WHEN search_unit.content_sha256 IS DISTINCT FROM EXCLUDED.content_sha256 THEN NULL
                ELSE search_unit.embedding_claim_token
            END,
            embedding_claimed_at = CASE
                WHEN %s THEN NULL
                WHEN search_unit.content_sha256 IS DISTINCT FROM EXCLUDED.content_sha256 THEN NULL
                ELSE search_unit.embedding_claimed_at
            END,
            embedding_status_detail = CASE
                WHEN %s THEN NULL
                WHEN search_unit.content_sha256 IS DISTINCT FROM EXCLUDED.content_sha256 THEN NULL
                ELSE search_unit.embedding_status_detail
            END,
            embedded_at = CASE
                WHEN %s THEN NULL
                WHEN search_unit.content_sha256 IS DISTINCT FROM EXCLUDED.content_sha256 THEN NULL
                ELSE search_unit.embedded_at
            END,
            updated_at = EXCLUDED.updated_at
        """,
        (
            unit["searchUnitId"],
            unit["sourceFileId"],
            artifact_id,
            unit["unitType"],
            unit["unitKey"],
            unit.get("title"),
            unit.get("sectionPath"),
            unit.get("pageStart"),
            unit.get("pageEnd"),
            unit.get("textContent") or "",
            _json_text(unit.get("metadataJson")),
            unit.get("embeddingStatus") or "PENDING",
            unit.get("contentSha256"),
            unit.get("indexId"),
            now,
            now,
            reset_embedding_status,
            reset_embedding_status,
            reset_embedding_status,
            reset_embedding_status,
            reset_embedding_status,
            reset_embedding_status,
        ),
    )


def _read_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def _read_jsonl(path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for line_no, line in enumerate(path.read_text(encoding="utf-8").splitlines(), start=1):
        stripped = line.strip()
        if not stripped:
            continue
        try:
            raw = json.loads(stripped)
        except json.JSONDecodeError as exc:
            raise ValueError(f"{path}:{line_no} is not valid JSON") from exc
        if not isinstance(raw, dict):
            raise ValueError(f"{path}:{line_no} must be a JSON object")
        rows.append(raw)
    return rows


def _json_text(value: Any) -> str:
    return json.dumps(value if value is not None else {}, ensure_ascii=False, sort_keys=True)


def _batched(rows: list[dict[str, Any]], size: int) -> Iterable[list[dict[str, Any]]]:
    for offset in range(0, len(rows), size):
        yield rows[offset: offset + size]


