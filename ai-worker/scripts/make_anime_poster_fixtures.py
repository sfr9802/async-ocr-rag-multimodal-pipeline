"""Generate synthetic anime poster fixture images.

Renders 6 minimal "poster" PNGs into ``ai-worker/fixtures/posters/`` —
each one combines a title, subtitle, a colored block, and a couple of
silhouette shapes so a vision provider has something visual to caption
beyond raw OCR. The output is intentionally placeholder-grade because
the multimodal anime eval is a demo signal, not a benchmark pillar.

Posters are rendered programmatically with Pillow, so every image is
original / self-authored with no license entanglement; we drop a
``LICENSE.md`` note in the directory to make that explicit.

Usage (from ``ai-worker/``)::

    python -m scripts.make_anime_poster_fixtures
"""

